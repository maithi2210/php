    $name = $_POST["name"];
    $email = $_POST["email"];
    $uname = $_POST["uname"];
    $pass = password_hash($_POST["pass"], PASSWORD_BCRYPT);

    $sql = $conn->prepare("INSERT INTO users (name, email, uname, password) VALUES (?, ?, ?, ?)");
    $sql->bind_param("ssss", $name, $email, $uname, $pass);

    if ($sql->execute()) {
        header("Location:login.php");
    } else {
        header("Location:register.php");
    }
}
